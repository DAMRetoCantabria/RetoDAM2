package reto.objects;

/**
 * Enumerado que representa los posibles estados de una actividad.
 * Los estados pueden ser: Aprobada, Solicitada, Denegada, Realizada.
 */
public enum EstadoActividad {
    /**
     * Estado que representa una actividad aprobada.
     */
    Aprobada,
    /**
     * Estado que representa una actividad solicitada.
     */
    Solicitada,
    /**
     * Estado que representa una actividad denegada.
     */
    Denegada,
    /**
     * Estado que representa una actividad realizada.
     */
    Realizada
}
