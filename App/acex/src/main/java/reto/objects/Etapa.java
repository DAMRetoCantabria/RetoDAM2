package reto.objects;

/**
 * Enumeración que representa las diferentes etapas educativas.
 */
public enum Etapa {
    /**
     * Representa la etapa de enseñanza secundaria obligatoria.
     */
    ESO,
    /**
     * Representa la etapa de bachillerato.
     */
    Bachillerato,
    /**
     * Representa la etapa de formacion profesional de grado superior.
     */
    FPGS,
    /**
     * Representa la etapa de formacion profesional de grado medio.
     */
    FPGM,
    /**
     * Representa la etapa de formacion profesional basica.
     */
    FPB,
    /**
     * Representa la etapa de formacon profesional de cursos de especializacion.
     */
    FPCE
}
