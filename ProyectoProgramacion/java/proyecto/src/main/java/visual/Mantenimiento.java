    public static final void csv(String filePath) throws IOException {
        listModel = new DefaultListModel<>();
        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] fields = line.split(SEPARATOR);
                // Agrega el primer campo como título a la lista
                if (fields.length > 0) {
                    listModel.addElement(fields[0]);
                }
            }
        } catch (IOException e) {
            throw new IOException("Error al leer el archivo CSV: " + e.getMessage(), e);
        }
    }

