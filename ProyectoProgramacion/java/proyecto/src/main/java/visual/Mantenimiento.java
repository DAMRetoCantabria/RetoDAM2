    private static void option(String opcion) {
        switch (opcion) {
            case "Cursos" -> {
                try {
                    csv("/Escritorio/cursos.csv");
                } catch (IOException e) {
                    System.out.println("Error al leer el archivo CSV: " + e.getMessage());
                }
                initializeJList();
            }
            case "Grupos" -> {
                try {
                    csv("/Escritorio/grupos.csv");
                } catch (IOException e) {
                    System.out.println("Error al leer el archivo CSV: " + e.getMessage());
                }
                initializeJList();
            }
            case "Departamentos" -> {
                try {
                    csv("/Escritorio/departamentos.csv");
                } catch (IOException e) {
                    System.out.println("Error al leer el archivo CSV: " + e.getMessage());
                }
                initializeJList();
            }
            default ->
                System.out.println("Opcion incorrecta.");
        }
    }

