    private void guardarContrasena(String username, String password) {
        String filename = "/Desktop/contrasenas.txt";

        try (PrintWriter writer = new PrintWriter(new FileWriter(filename, true))) {
            writer.println(username + "," + password);
        } catch (IOException e) {
            System.err.println("Error al guardar la contrase침a: " + e.getMessage());
        }
    }
